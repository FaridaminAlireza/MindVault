"""
The sine formula of tirangles

let's △ ABC, 
angles A,B,C
and opposite side of angles a,b,c

From vertx A draw a vertical line
that intercepts the base a or its extent.
call the interception point as D and AD as h1.

Sin B =  h1 /c -> h1 = sin B * c
eq1) area of △ ABC =  1/2 (sin B * c) * a 

Sin C = h1 /b -> h1 = sin C * b
eq2) area of △ ABC =  1/2 (sin C * b) * a

Now from vertx B draw a vertical line
that intercepts the base b or its extent.
call the interception point as E and BD as h2.

Sin A =  h2 /c -> h2 = sin A * c
eq3) area of △ ABC =  1/2 (sin A * c) * b

Puting the three equations together:

eq1) area of △ ABC =  1/2 (sin B * c) * a
eq2) area of △ ABC =  1/2 (sin C * b) * a
eq3) area of △ ABC =  1/2 (sin A * c) * b 

1/2 (sin B * c) * a = 1/2 (sin C * b) * a = 1/2 (sin A * c) * b 

Dividing the equations over 1/2 abc gives:

-> sin B / b = sin C / c = sin A / a 
This is called as the sine formula of triangles,
or the sine formula.
"""