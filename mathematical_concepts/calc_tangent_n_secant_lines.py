
# DIFFERENCE BETWEEN SECANT LINE AND TANGENT LINE OF A FUNCTION

# The primary difference between a secant line and a tangent line is
#  the number of points at which they intersect a curve.

# SECANT LINE
# Intersection points: Intersects a curve at two or more points.
# Rate of change: Represents the average rate of change of the function
#  between the two intersection points.
# Slope calculation: The slope is calculated using 
# the standard slope formula: m = (f(x2) - f(x1)) / (x2 - x1).
# Relationship to calculus: The concept of a secant line is the 
# foundation for finding the derivative. As the two intersection points 
# of a secant line get closer together, the secant line approaches the tangent line.
# Purpose: Used to determine the overall trend or average change over an interval.

# TANGENT LINE
# Intersection points: Touches a curve at 
# exactly one point, known as the point of tangency.
# Rate of change: Represents the instantaneous rate of 
# change of the function at the single point of tangency.
# Slope calculation: The slope is found by taking the derivative
# of the function at the point of tangency.
# Relationship to calculus: The tangent line is a visual representation
# of the derivative of a function at a specific point.
# Purpose: Used to determine the direction and "steepness" 
# of the curve at a precise, single point.

# HOW THEY RELATE TO EACH OTHER
# The slope of the tangent line is the limiting value of 
# the slope of the secant line as one of the intersection points
#  approaches the other. This is fundamental to the definition of
#  a derivative in calculus.